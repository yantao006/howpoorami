#!/usr/bin/env node
/**
 * Fetches the richest person per country from the komed3/rtb-api
 * (Forbes Real-Time Billionaires mirror) and updates billionaires.ts.
 *
 * Source: https://github.com/komed3/rtb-api (MIT license)
 * Data origin: Forbes Real-Time Billionaires
 *
 * Usage: node scripts/update-billionaires.mjs
 */

import { writeFileSync, readFileSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUTPUT_PATH = resolve(__dirname, "../src/data/billionaires.ts");

const RTB_BASE = "https://cdn.statically.io/gh/komed3/rtb-api/main/api";

// Map from rtb-api 2-letter country codes to our app's country codes
// rtb-api uses lowercase ISO 3166-1 alpha-2
const COUNTRY_CODES = [
  "us", "gb", "fr", "de", "nl", "ca", "au", "jp", "kr", "cn",
  "in", "br", "mx", "za", "se", "no", "dk", "fi", "ch", "it",
  "es", "sg", "ie", "nz", "cl", "at", "be", "pl", "pt", "cz",
];

async function fetchJSON(url) {
  const res = await fetch(url);
  if (!res.ok) throw new Error(`HTTP ${res.status} for ${url}`);
  return res.json();
}

/**
 * The _list endpoint returns space-separated text:
 * "us 930 8290927.375 elon-musk 1 839618.478"
 * Fields: country count totalNetWorth topSlug topRank topNetWorth
 * Net worth values are in millions USD.
 */
async function fetchCountryList() {
  const url = `${RTB_BASE}/stats/country/_list`;
  console.log(`Fetching country stats from ${url}`);
  const res = await fetch(url);
  if (!res.ok) throw new Error(`HTTP ${res.status} for ${url}`);
  const text = await res.text();

  const result = {};
  for (const line of text.trim().split("\n")) {
    const parts = line.trim().split(/\s+/);
    if (parts.length < 6) continue;
    const [code, count, totalNW, topSlug, topRank, topNW] = parts;
    result[code] = {
      count: parseInt(count),
      totalNetWorth: parseFloat(totalNW), // millions USD
      topSlug,
      topRank: parseInt(topRank),
      topNetWorth: parseFloat(topNW), // millions USD
    };
  }
  return result;
}

async function fetchProfileInfo(slug) {
  const url = `${RTB_BASE}/profile/${slug}/info`;
  return fetchJSON(url);
}

function formatNetWorth(billions) {
  return Math.round(billions * 1_000_000_000);
}

async function main() {
  console.log("Fetching billionaire data from rtb-api (Forbes mirror)...\n");

  const countryStats = await fetchCountryList();

  const entries = [];
  const errors = [];

  for (const code of COUNTRY_CODES) {
    const stats = countryStats[code];
    if (!stats) {
      errors.push(`  No data for country: ${code.toUpperCase()}`);
      continue;
    }

    // Fetch profile info for the richest person to get their name and industry
    let name = "Unknown";
    let source = "";
    let photoDesc = "";
    try {
      const info = await fetchProfileInfo(stats.topSlug);
      name = info.name || stats.topSlug;
      // source can be an array of industries
      const rawSource = info.source;
      source = Array.isArray(rawSource) ? rawSource.join(", ") : (rawSource || "");
      if (!source) {
        const industries = info.industries;
        source = Array.isArray(industries) ? industries.join(", ") : "";
      }
      // organization can be an object with name/title or a string
      const org = info.organization;
      if (org && typeof org === "object") {
        photoDesc = [org.title, org.name].filter(Boolean).join(" of ");
      } else {
        photoDesc = info.title || (typeof org === "string" ? org : "") || "";
      }
    } catch {
      name = stats.topSlug.replace(/-/g, " ").replace(/\b\w/g, (c) => c.toUpperCase());
    }

    const upper = code.toUpperCase();
    // Net worth from _list is in millions; convert to absolute USD
    const netWorth = Math.round(stats.topNetWorth * 1_000_000);

    entries.push({
      code: upper,
      name,
      netWorth,
      source: source || "Forbes Real-Time Billionaires",
      country: upper,
      photoDesc: photoDesc || name,
    });

    console.log(
      `  ${upper}: ${name} — $${(stats.topNetWorth / 1000).toFixed(1)}B`
    );
  }

  // Find global richest
  let globalRichest = entries[0];
  for (const e of entries) {
    if (e.netWorth > globalRichest.netWorth) globalRichest = e;
  }

  const today = new Date().toISOString().split("T")[0];

  // Generate the TypeScript file
  const tsContent = `/**
 * Richest person per country data.
 *
 * AUTO-GENERATED by scripts/update-billionaires.mjs on ${today}
 * Source: Forbes Real-Time Billionaires via komed3/rtb-api
 *         https://github.com/komed3/rtb-api (MIT license)
 *
 * To update: node scripts/update-billionaires.mjs
 */

export interface BillionaireData {
  readonly name: string;
  readonly netWorth: number; // USD
  readonly source: string;
  readonly country: string;
  readonly photoDescription: string;
}

export const RICHEST_BY_COUNTRY: Readonly<Record<string, BillionaireData>> = {
${entries
  .map(
    (e) => `  ${e.code}: {
    name: ${JSON.stringify(e.name)},
    netWorth: ${e.netWorth.toLocaleString("en-US").replace(/,/g, "_")},
    source: ${JSON.stringify(e.source)},
    country: ${JSON.stringify(e.country)},
    photoDescription: ${JSON.stringify(e.photoDesc)},
  },`
  )
  .join("\n")}
  GLOBAL: {
    name: ${JSON.stringify(globalRichest.name)},
    netWorth: ${globalRichest.netWorth.toLocaleString("en-US").replace(/,/g, "_")},
    source: ${JSON.stringify(globalRichest.source)},
    country: "GLOBAL",
    photoDescription: "World's richest person",
  },
} as const;
`;

  // Read the existing file to preserve DETAILED_SHARES and other exports
  const existing = readFileSync(OUTPUT_PATH, "utf-8");
  const detailedSharesStart = existing.indexOf("/**\n * Detailed wealth shares");
  if (detailedSharesStart === -1) {
    // No DETAILED_SHARES section — write just the billionaire data
    writeFileSync(OUTPUT_PATH, tsContent);
  } else {
    // Preserve everything from DETAILED_SHARES onward
    const preserved = existing.slice(detailedSharesStart);
    writeFileSync(OUTPUT_PATH, tsContent + "\n" + preserved);
  }

  console.log(`\n✓ Updated ${OUTPUT_PATH}`);
  console.log(`  ${entries.length} countries, generated ${today}`);
  if (errors.length > 0) {
    console.log("\nWarnings:");
    errors.forEach((e) => console.log(e));
  }
}

main().catch((err) => {
  console.error("Failed to update billionaires:", err);
  process.exit(1);
});
